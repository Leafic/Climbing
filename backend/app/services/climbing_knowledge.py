TECHNIQUE_REFERENCE = """[클라이밍 핵심 기술 레퍼런스 — 영상에서 관찰될 때만 언급하세요]

[그립 기술]
• 크림프(Crimp): 손가락을 접어 첫마디 관절을 세움. 작은 홀드에서 강한 힘. 부상 위험 있음
• 하프크림프(Half Crimp): 크림프보다 약간 열린 각도. 일상적으로 가장 많이 쓰이는 그립
• 오픈핸드(Open Hand): 손가락 관절을 열고 마찰력으로 잡음. 슬로퍼, 둥근 홀드에 적합
• 핀치(Pinch): 엄지+검지~약지로 양쪽에서 누르듯 잡음. 돌출형 홀드에 사용

[발 기술]
• 에징(Edging): 신발 안쪽(인사이드 에지) 또는 바깥(아웃사이드 에지)으로 작은 홀드를 밟음
• 스미어링(Smearing): 발바닥 전체의 마찰력으로 경사면을 밟음. 발 홀드가 없을 때 사용
• 힐훅(Heel Hook): 뒤꿈치를 홀드 위에 걸어 체중 분산. 오버행에서 필수
• 토훅(Toe Hook): 발등을 홀드 아래에 걸어 당김. 루프(천장) 구간에서 핵심
• 바이시클(Bicycle): 힐훅+토훅을 같은 홀드에 동시 적용. 강한 고정력

[무브 기술]
• 데드포인트(Deadpoint): 동적으로 이동하여 속도가 0이 되는 순간 홀드를 잡음
• 랜지(Lunge/Dyno): 두 발을 벽에서 떼고 점프하여 먼 홀드를 잡는 동적 무브
• 플래깅(Flagging): 사용하지 않는 발을 안쪽/바깥쪽으로 뻗어 카운터밸런스
  - 인사이드 플래그: 지지하는 발 뒤로 교차하여 반대쪽으로 뻗음
  - 아웃사이드 플래그: 같은 쪽 바깥으로 뻗음
• 드롭니(Drop Knee): 무릎을 안쪽으로 떨어뜨려 골반을 벽에 붙임. 리치 확장
• 매칭(Matching): 한 홀드에 양손/양발을 모두 올림
• 가스통(Gaston): 팔꿈치를 바깥으로 벌려 홀드를 밀듯이 잡음
• 언더클링(Undercling): 홀드를 아래에서 위로 당기듯 잡음. 팔을 낮춘 상태에서 유리

[체중 이동 원리]
• 3점 지지: 4개 접점 중 3개를 유지한 채 1개만 이동 → 안정성 극대화
• 대각선 원리: 오른손을 뻗으려면 왼발에 체중 → 자연스러운 대각선 밸런스
• 힙 포지셔닝: 다음 홀드 방향으로 골반을 미리 돌려놓으면 리치가 15~20cm 증가
• 레스트 포지션: 좋은 홀드에서 팔을 번갈아 내려 흔들어(쉐이크아웃) 혈류 회복
"""


SUCCESS_ANALYSIS_CRITERIA = """[성공 영상 분석 기준 — 단순 "잘했다"가 아닌 구체적 기술 평가]

성공 영상이라도 아래 관점에서 기술 완성도를 평가하세요:

1. **효율성**: 최소한의 에너지로 등반했는가? 불필요한 동작이 없었는가?
2. **유연성**: 대안 시퀀스가 더 효율적이었을 가능성은?
3. **안정성**: 위태로운 순간 없이 안정적으로 올랐는가? (아슬아슬한 성공 vs 여유로운 성공)
4. **페이스**: 구간별 속도 조절이 적절했는가? 쉬운 구간은 빠르게, 크럭스는 신중하게?
5. **다음 난이도 준비도**: 이 수준의 루트를 안정적으로 클리어하면 다음 단계에 필요한 기술은?
"""


def get_knowledge_prompt(skill_level: str, attempt_result: str) -> str:
    """숙련도와 시도 결과에 맞는 도메인 지식 프롬프트를 반환"""
    parts = []

    # 실패 패턴 (해당 숙련도)
    failure_pattern = FAILURE_PATTERNS.get(skill_level, FAILURE_PATTERNS["beginner"])
    parts.append(failure_pattern)

    # 기술 레퍼런스 (모든 레벨 공통)
    parts.append(TECHNIQUE_REFERENCE)

    # 완등 확률 기준
    if attempt_result == "failure":
        parts.append(COMPLETION_CRITERIA)
    else:
        parts.append(SUCCESS_ANALYSIS_CRITERIA)

    return "\n".join(parts)

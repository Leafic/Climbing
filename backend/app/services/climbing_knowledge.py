COMPLETION_CRITERIA = """[완등 확률 판단 기준 — 프로 코치 관점]

completionProbability를 판단할 때 아래 5가지 요소를 종합 평가하세요:

1. **진행도** (어디까지 갔는가)
   - 전체 루트의 몇 %까지 진행했는지
   - 크럭스를 통과했는지 vs 크럭스 전에 떨어졌는지
   - 크럭스 이후 떨어진 경우: 체력 문제일 가능성 높음 → 확률 높게
   - 크럭스 전에 떨어진 경우: 기술/시퀀스 문제 → 확률 보통

2. **실패 원인의 교정 가능성**
   - 발 미끄러짐 (자주 교정 가능) → +10~15%
   - 시퀀스 오류 (대안 있으면 교정 가능) → +10~20%
   - 리치 부족 (체격 한계) → +0~5%
   - 펌핑 (체력 문제, 점진 개선) → +5~10%

3. **동작 품질**
   - 기본기(팔 펴기, 발끝 사용 등)가 되어 있는가
   - 체중 이동이 자연스러운가
   - 불필요한 동작(재조정, 허둥댐)이 적은가

4. **심리 상태**
   - 크럭스 전에 주저하는가 vs 자신감 있게 진입하는가
   - 떨어진 후 패닉인가 vs 침착한가 (재시도 시 유리)

5. **에너지 관리**
   - 쉬운 구간에서 쉬는 타이밍을 잡는가
   - 크럭스까지 도달 시 에너지가 남아있었는가
"""

